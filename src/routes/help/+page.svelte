<h1>How to use Listodon</h1>
<p>
  If you have multiple distinct interests—e.g. politics and tech—and you follow people in both
  arenas, Mastodon will intermingle their posts.
</p>
<p>
  If you don’t like the intermingling, you can make Lists for your topics of interest: a Politics
  list and a Tech list. Whenever you follow someone you can add them to one of your lists. (Mastodon
  doesn’t notify them.)
</p>
<p>
  But what if you follow people and forget to add them to a list? Listodon will tell you. The first
  time you run it, it asks for permission to look at your account. Then it looks at everyone you
  follow, and makes sure you’ve remembered to put them all in lists. It shows you everyone you
  haven’t put in a list.
</p>
<p>How do you fix it? Follow the link to the person’s profile on Mastodon and click:</p>
<img src="help.png" class="helpimage" alt="In Mastodon, click Add or Remove from lists" />
<p>You can come back to Listodon any time you want and re-check.</p>

