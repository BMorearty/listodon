<h1>How to use Listodon</h1>
<p>
  If you have multiple distinct interests—e.g. politics and tech—and you follow people in both
  arenas, Mastodon will intermingle their posts. To solve this, you can make Lists for your topics
  of interest. Whenever you follow someone you can add them to one of your lists. (They won’t get
  notified.)
</p>
<p>
  But what if you follow people and forget to add them to a list? Listodon will tell you. First it
  asks for permission to look at your account. Then it looks at everyone you follow, and makes sure
  you’ve remembered to put them all in lists. It shows you everyone you haven’t put in a list.
</p>
